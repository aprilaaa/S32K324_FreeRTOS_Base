sint32  bsp_i2c0_send(uint32 addr, uint8 *buf, uint32 len)
{
	I2c_RequestType req;
	Std_ReturnType ret;

	req.SlaveAddress         = (addr >> 1);   // 7-bit 地址
	req.BitsSlaveAddressSize = FALSE;         // 7-bit
	req.ExpectNack           = FALSE;
	req.RepeatedStart        = FALSE;
	req.BufferSize           = len;
	req.DataDirection        = I2C_SEND_DATA;
	req.DataBuffer           = buf;

	ret = I2c_SyncTransmit(I2C_CHANNEL_OLED, &req);
}


static uint32  dev_ssd1306_write_cmd(uint8 addr, uint8 cmd)
{
	uint8 send_data[2];
	send_data[0]=0;
	send_data[1]=cmd;
	return bsp_i2c0_send(addr,send_data,sizeof(send_data));
}

static sint32  dev_ssd1306_write_data(uint8 addr, uint8 data)
{
	uint8 send_data[2];
	send_data[0]=0x40;
	send_data[1]=data;
	return bsp_i2c0_send(addr,send_data,sizeof(send_data));
}

